class ArithmeticOperator {
	public static void main(String[] args) {
		int fnum = 100;
		int snum = 50;
		System.out.println("  First num = "+fnum+"         Second num = "+snum);
		System.out.println("-----------------------------------------------------");
		System.out.println(fnum+snum);
		System.out.println("-----------------------------------------------");
		System.out.println(fnum-snum);
		System.out.println("-----------------------------------------------");
		System.out.println(fnum*snum);
		System.out.println("-----------------------------------------------");
		System.out.println(fnum/snum);
		System.out.println("-----------------------------------------------");
		System.out.println(fnum%snum);
	}
	
}